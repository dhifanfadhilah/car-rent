    private void adminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminButtonActionPerformed
        // TODO add your handling code here:
        if(usernameField.getText().equals("") && passwordField.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Please Complete the Fields");
        } else if(usernameField.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Please Complete the Username Field");
        } else if(passwordField.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Please Complete the Password Field");
        } else{
            Koneksi kon = new Koneksi();
            ResultSet rs = null;
            try{
                rs = kon.getData("select * from user where username = '"+usernameField.getText()+"' and password = '"+passwordField.getText()+"' and code = '1'");
                if(rs.next() == false){
                    javax.swing.JOptionPane.showMessageDialog(null, "Anda tidak dapat login sebagai admin");
                } else{
                    do{
                        javax.swing.JOptionPane.showMessageDialog(null, "Login Success");
                        Admin form = new Admin();
                        form.setVisible(true);
                        dispose();
                        usernameField.setEnabled(false);
                        passwordField.setEnabled(false);
                        adminButton.setEnabled(false);
                        employeeButton.setEnabled(false);
                    }while(rs.next());
                }
                rs.close();
            } catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, "Login Failed");
            }
        }
    }//GEN-LAST:event_adminButtonActionPerformed

