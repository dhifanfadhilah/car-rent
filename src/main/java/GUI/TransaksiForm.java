    private void listMobilTablePropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_listMobilTablePropertyChange
        // TODO add your handling code here:
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        int i = 0;
        try{
            rs = kon.getData("select * from cars");
            while(rs.next()){
                listMobilTable.setValueAt(rs.getString("car_id"), i, 0);
                listMobilTable.setValueAt(rs.getString("car_name"), i, 1);
                listMobilTable.setValueAt(rs.getString("rent"), i, 2);
                listMobilTable.setValueAt(rs.getString("stock"), i, 3);
                i++;
            }
            rs.close();
        } catch(Exception e){

        }
    }//GEN-LAST:event_listMobilTablePropertyChange

    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        // TODO add your handling code here:
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        try{
            rs = kon.getData("select * from cars where car_id = '"+idMobilField.getText()+"'");
            while(rs.next()){
                biayaField.setText(rs.getString("rent"));
                int k, l, m, n, o;
                k = Integer.valueOf(lamaField.getText()).intValue();
                l = Integer.valueOf(""+tglComboBox.getSelectedItem()).intValue();
                m = Integer.valueOf(biayaField.getText()).intValue();
                n = k + l;
                o = k * m;
                totalField.setText(String.valueOf(o));
                kembaliField.setText(thnField.getText()+"-"+blnComboBox.getSelectedItem()+"-"+String.valueOf(n));
            }
            rs.close();
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_generateButtonActionPerformed

    private void savePinjamButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePinjamButtonActionPerformed
        // TODO add your handling code here:
        Peminjaman pem = new Peminjaman();
        pem.setIdPinjam(idPinjamField.getText());
        pem.setIdCust(idPelField.getText());
        pem.setIdPegawai(idPegawaiField.getText());
        pem.setIdMobil(idMobilField.getText());
        pem.setPhari(""+tglComboBox.getSelectedItem());
        pem.setPbulan(""+blnComboBox.getSelectedItem());
        pem.setPtahun(thnField.getText());
        pem.setLama(lamaField.getText());
        pem.setTglKembali(kembaliField.getText());
        pem.setBiaya(biayaField.getText());
        pem.insertPeminjaman();
        javax.swing.JOptionPane.showMessageDialog(null, "Insert Success!");
        
        idPinjamField.setText(null);
        idPelField.setText(null);
        idPegawaiField.setText(null);
        idMobilField.setText(null);
        tglComboBox.setSelectedIndex(0);
        blnComboBox.setSelectedIndex(0);
        thnField.setText(null);
        lamaField.setText(null);
        kembaliField.setText(null);
        biayaField.setText(null);
    }//GEN-LAST:event_savePinjamButtonActionPerformed

