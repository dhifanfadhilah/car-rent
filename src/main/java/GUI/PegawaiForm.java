    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String jabatan = "";
        
        
        idResultField.setEnabled(false);
        namaResultField.setEnabled(false);
        usernameResultField.setEnabled(false);
        passwordResultField.setEnabled(false);
        kodeResultField.setEnabled(false);
        
        Pegawai cust = new Pegawai();
        cust.setId(idResultField.getText());
        cust.setName(namaResultField.getText());
        cust.setUsername(usernameResultField.getText());
        cust.setPassword(passwordResultField.getText());
        cust.setCode(kodeResultField.getText());
        cust.setJabatan(jabatan);
        
        if(kodeResultField.getText() == "1"){
            cust.updatePegawaiAdmin();
        } else if(kodeResultField.getText() == "2"){
            cust.updatePegawai();
        }
        
        javax.swing.JOptionPane.showMessageDialog(null, "Update Success!");
        showData();
    }//GEN-LAST:event_updateButtonActionPerformed

