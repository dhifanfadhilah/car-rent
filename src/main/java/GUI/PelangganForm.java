    public void showData(){
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        int i = 0;
        try{
            rs = kon.getData("select * from customer");
            while(rs.next()){
                custTable.setValueAt(rs.getString("customer_id"), i, 0);
                custTable.setValueAt(rs.getString("name"), i, 1);
                custTable.setValueAt(rs.getString("address"), i, 2);
                custTable.setValueAt(rs.getString("phone_number"), i, 3);
                i++;
            }
            rs.close();
        } catch(Exception e){

        }
    }

