    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        try{
            rs = kon.getData("select * from customer where customer_id = '"+idSearchField.getText()+"'");
            while(rs.next()){
                idResultField.setText(rs.getString("customer_id"));
                namaResultField.setText(rs.getString("name"));
                alamatResult.setText(rs.getString("address"));
                numResultField.setText(rs.getString("phone_number"));
                javax.swing.JOptionPane.showMessageDialog(null, "Search Success!");
                idSearchField.setText(null);
                namaResultField.setEnabled(true);
                alamatResult.setEnabled(true);
                numResultField.setEnabled(true);
                updateButton.setEnabled(true);
                deleteButton.setEnabled(true);
            }
            rs.close();
        } catch(Exception e){

        }
    }//GEN-LAST:event_searchButtonActionPerformed

