    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        try{
            rs = kon.getData("select * from cars where car_id = '"+idSearchField.getText()+"'");
            while(rs.next()){
                idResultField.setText(rs.getString("car_id"));
                jenisResultField.setText(rs.getString("car_name"));
                sewaResultField.setText(rs.getString("rent"));
                stokResultField.setText(rs.getString("stock"));
                javax.swing.JOptionPane.showMessageDialog(null, "Search Success!");
                idSearchField.setText(null);
                jenisResultField.setEnabled(true);
                sewaResultField.setEnabled(true);
                stokResultField.setEnabled(true);
                updateButton.setEnabled(true);
                deleteButton.setEnabled(true);
            }
            rs.close();
        } catch(Exception e){

        }
    }//GEN-LAST:event_searchButtonActionPerformed

